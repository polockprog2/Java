package classes;
import java.lang.*;

public class Nurse
{
	private String name;
	private String nurseId;
	
	public void setName(String name)
	{
		this.name = name;
	}
	public void setNurseId(String nurseId)
	{
		this.nurseId = nurseId;
	}
	public String getName()
	{
		return name;
	}
	public String getNurseId()
	{
		return nurseId;
	}
}