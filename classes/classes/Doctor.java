package classes;
import java.lang.*;

public class Doctor
{
	private String name;
	private char gender;
	private String docId;
	
	public void setName(String name)
	{
		this.name = name;
	}
	public void setGender()
	{
		this.gender = gender;
	}
	public void setDocId(String docId)
	{
		this.docId = docId;
	}
	public String getName()
	{
		return name;
	}
	public char getGender()
	{
		return gender;
	}
	public String getDocId()
	{
		return docId;
	}

}