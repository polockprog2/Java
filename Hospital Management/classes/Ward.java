package classes;
import java.lang.*;

public class Ward
{
	 protected String name;
	 protected String wardNumber;
	
	
	public void setName(String name)
	{
         this.name = name;		
	}
	public void setWardNumber(String wardNumber)
	{
		this.wardNumber = wardNumber;
	}
	
	public String getName()
	{
		return name;
	}
	public String getWardNumber()
	{
		return wardNumber;
	}

}