package classes;
import java.lang.*;

public class Nurse
{
	private String name;
	private String nid;
	
	public void setName(String name)
	{
		this.name = name;
	}
	public void setNid(String nid)
	{
		this.nid = nid;
	}
	public String getName()
	{
		return name;
	}
	public String getNid()
	{
		return nid;
	}
}